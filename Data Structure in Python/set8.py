# Example 8: Checking Membership in a Set
# You can check the membership of an element in a set using the 'in' and 'not in' operators.

# Example Set
my_set = {1, 2, 3, 4, 5}

# Check if an element is present in the set
if 3 in my_set:
    print('Element 3 is present in the set')
else:
    print('Element 3 is not present in the set')

# Check if an element is not present in the set
if 6 not in my_set:
    print('Element 6 is not present in the set')
else:
    print('Element 6 is present in the set')