# Example 4: Adding Elements to a Tuple
# Tuples are immutable, so you cannot add or remove elements from a tuple directly.
# However, you can concatenate two or more tuples to create a new tuple.

# Example tuples
my_tuple = (1, 2, 3)

# Adding elements by creating a new tuple
my_tuple = my_tuple + (4, 5)

# Print the updated tuple
print('Tuple after adding elements:', my_tuple)
