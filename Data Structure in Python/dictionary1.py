# Example 1: Creating a Dictionary
# A dictionary is a collection of key-value pairs enclosed in curly braces {}.
# Each key is unique, and it is used to access the corresponding value.
# You can create an empty dictionary using the dict() constructor or by using curly braces {}.

# Create an dictionary using curly braces with various key-value pairs
my_dict = {'name': 'Elon', 'age': 26, 'city': 'Los Angeles'}

# Create an empty dictionary using the dict() constructor
empty_dict = dict()

# Print the dictionaries
print('Created Dictionary:', my_dict)
print('Empty Dictionary:', empty_dict)