# Example 8: Checking Membership in a List
# You can check if an element is present in a list using the 'in' keyword. The in operator returns True if the element is present in the list and False otherwise.

# Example list
my_list = ['Mark', 'Elon', 'Jeff', 'Bill', 'Warren']

# Checking for membership
# Check if 'Elon' is present in the list

if 'Elon' in my_list:
    print('Elon is present in the list')
else:
    print('Elon is not present in the list')