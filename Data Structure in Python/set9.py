# Example 9: Iterating Over a Set
# You can iterate over a set to access each element using a for loop. Since sets are unordered, the order of elements during iteration may be returned in any order.

# Example sets
my_set = {1, 2, 3, 4, 5}

# Iterate over the set
print('Set Elements:')
for element in my_set:
    print('Element:', element)