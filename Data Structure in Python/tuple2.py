# Example 2: Accessing Tuple Elements
# Elements in a tuple are accessed using indices, smilar to lists.
# The index of the first element is 0, the second element is 1, and so on.
# You can access elements in a tuple using square brackets [] and the index of the element you want to access.

# Example tuple
my_tuple = (1, 2, 3, 4, 5)

# Access the first element of the tuple
first_element = my_tuple[0]

# Access the second element of the tuple
second_element = my_tuple[1]

# Access the last element of the tuple (using negative index)
last_element = my_tuple[-1]

# Print the accessed elements
print('First Element:', first_element)
print('Second Element:', second_element)
print('Last Element:', last_element)
