# Example 1: Creating a List
# A list is a collection of items in Python, created using square brackets [].
# List can contain any type of data type, including strings, integers, and even other lists.
# Items in a list are separated by commas.
# Lists are mutable, meaning that you can change the contents of a list after you create it.

# Create a list with various data types
my_list = [1, 2, 3, 'a', 'b', 'c', [4, 5, 6], 'Python', 'Java', 'C++']

# Print the list
print('Created List:', my_list)