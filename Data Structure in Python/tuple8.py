# Example 8: Checking Membership in a Tuple
# You can check if an element is present in a tuple using the 'in' keyword.
# The 'in' keyword returns True if the element is present in the tuple, and False otherwise.

# Example tuple
my_tuple = ('Elon', 'Jeff', 'Bill', 'Warren', 'Mark')

# Check if 'Bill' is present in the tuple
if 'Bill' in my_tuple:
    print('Bill is present in the tuple')
else:
    print('Bill is not present in the tuple')
