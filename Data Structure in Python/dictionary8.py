# Example 8: Iterating Over a Dictionary
# You can iterate over a dictionary using a for loop to access the keys, values, or key-value pairs.

# Example Dictionary
my_dict = {'name': 'Elon', 'age': 26, 'city': 'Los Angeles'}

# Iterate over the keys and values
for key, value in my_dict.items():
    print(f'Key: {key}, Value: {value}')