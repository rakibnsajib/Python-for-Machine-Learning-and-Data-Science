# Example 3: Modifying Dictionary Values
# Dictionaries are mutable, meaning you can modify the values of a dictionary after it has been created.
# You can modify the value of a specific key by using the assignment operator (=) and the key.

# Example Dictionary
my_dict = {'name': 'Elon', 'age': 26, 'city': 'Los Angeles'}

# Modify the value of the 'age' key
my_dict['age'] = 30

# Print the modified dictionary
print('Modified Dictionary:', my_dict)