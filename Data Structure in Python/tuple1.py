# Example 1: Creating a Tuple
# A tuple is a collection of elements enclosed in parentheses ().
# Tuples are similar to lists, but they are immutable, meaning that the elements cannot be changed or modified after the tuple is created.
# Tuples are ordered, meaning that the elements are stored in a specific order.
# Tuple elements can be of different data types, such as integers, floats, strings, lists, or other tuples.

# Create a tuple with various data types
my_tuple = (1, 2.5, 'Python', [4, 5], ('Java', 7))

# Print the tuple
print('Created Tuple:', my_tuple)