# Example 1: Creating a Set
# A set is a collection of unique elements enclosed in curly braces {} or created using the set() constructor.
# Sets are unordered, meaning that the elements are not stored in any particular order.

# Create a set with some elements using curly braces
my_set = {1, 2, 3, 'Python', 'Java', 5}

# Print the set
print('Created Set:', my_set)