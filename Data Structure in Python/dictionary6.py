# Example 6: Dictionary Keys, Values, and Items
# You can retrieve all the keys, values, or key-value pairs from a dictionary using the keys(), values(), and items() methods, respectively.

# Example Dictionary
my_dict = {'name': 'Elon', 'age': 26, 'city': 'Los Angeles'}

# Retrieve all the keys from the dictionary
keys = my_dict.keys()

# Retrieve all the values from the dictionary
values = my_dict.values()

# Retrieve all the key-value pairs from the dictionary
items = my_dict.items()

# Print the keys, values, and items
print('Keys:', keys)
print('Values:', values)
print('Items:', items)